    public void configCampos(Alocacao novaAlocacao) {
        for (int i = 0; i < listaEmpresa.size(); i++) {
            if(listaEmpresa.get(i).id.equals(novaAlocacao.getEmpresa_id())) {
                comboLocacaoEmpresa.setSelectedIndex(i);
            }
        }
        for (int i = 0; i < listaFunc.size(); i++) {
            if(listaFunc.get(i).id.equals(novaAlocacao.getFunc_id())) {
                comboLocacaoFunc.setSelectedIndex(i);
            }
        }
        txtDetalhesLocacao.setText(novaAlocacao.getDetalhes());
        txtQtdHorasLocacao.setText(novaAlocacao.getQuantidades_horas());
        txtTipoLocacao.setText(novaAlocacao.getTipo_servico());
    }
    
    private void puxarListaEmpresa() {
        this.conectar.conectaBanco();
        
        try {
            this.conectar.executarSQL("SELECT empresa_razao_social,empresa_id from empresa");
            while(this.conectar.getResultSet().next()) {
               EmpresaAlocacao empresaDados = new EmpresaAlocacao();
                empresaDados.razaoSocial = this.conectar.getResultSet().getString(1);
                empresaDados.id = this.conectar.getResultSet().getString(2);
                listaEmpresa.add(empresaDados);
            }
        } catch (Exception e) {
            System.out.println(e);
        } finally {
            this.conectar.fechaBanco();
        }
    }
    
    private void puxarListaFuncionarios() {
        this.conectar.conectaBanco();
        
        try {
            this.conectar.executarSQL("SELECT funcionario_nome,funcionario_id from funcionarios");
            while(this.conectar.getResultSet().next()) {
                FuncAlocacao funcDados = new FuncAlocacao();
                funcDados.nome = this.conectar.getResultSet().getString(1);
                funcDados.id = this.conectar.getResultSet().getString(2);
                listaFunc.add(funcDados);
            }
        } catch (Exception e) {
        } finally {
            this.conectar.fechaBanco();
        }
    }
    
    private void popularComboEmpresa() {
        for (int i = 0; i < listaEmpresa.size(); i++) {
            comboLocacaoEmpresa.addItem(listaEmpresa.get(i).razaoSocial);
        }
    }
    
    private void popularComboFunc() {
        for (int i = 0; i < listaFunc.size(); i++) {
            comboLocacaoFunc.addItem(listaFunc.get(i).nome);
        }
    }
    
