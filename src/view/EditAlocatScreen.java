    private void btnSalvarAlocacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarAlocacaoActionPerformed
        this.conectar.conectaBanco();
        
        try {
            
            String id_func = listaFunc.get(comboLocacaoFunc.getSelectedIndex()).id;
            String id_empresa = listaEmpresa.get(comboLocacaoEmpresa.getSelectedIndex()).id;
            
            this.conectar.updateSQL("UPDATE alocacao set alocacao_prestador_id ='"+id_func+"', alocacao_empresa_id ='"+id_empresa+"' ,alocacao_detalhes ='"+txtDetalhesLocacao.getText()+"', alocacao_quantidade_horas ='"+txtQtdHorasLocacao.getText()+"', alocacao_tipo_servico ='"+txtTipoLocacao.getText()+"' where alocacao_id = '"+this.alo_id+"'");
            
            JOptionPane.showMessageDialog (null, "Alocacao editada no banco de dados", "Editada", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error",
            "Verifique os dados digitados", JOptionPane.ERROR_MESSAGE);
            System.out.println(e);
        } finally {
            this.conectar.fechaBanco();
        }
    }//GEN-LAST:event_btnSalvarAlocacaoActionPerformed

