    private void btnConsultarEmpresa1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarEmpresa1ActionPerformed
         if(txtConsultaAlocacao.getText().length() == 0 ) {
            JOptionPane.showMessageDialog(null, "Verifique os dados digitados",
            "Error!", JOptionPane.ERROR_MESSAGE);
        }
        
        else {
            Alocacao consultadoFunc = new Alocacao();
            this.conectar.conectaBanco();
            try {
                this.conectar.executarSQL("SELECT alocacao_prestador_id,alocacao_empresa_id,alocacao_detalhes,alocacao_quantidade_horas,alocacao_tipo_servico from alocacao where alocacao_id = "+ "'" + txtConsultaAlocacao.getText()+ "'" + ";");
                while(this.conectar.getResultSet().next()) {
                    consultadoFunc.setFunc_id(this.conectar.getResultSet().getString(1));
                    consultadoFunc.setEmpresa_id(this.conectar.getResultSet().getString(2));
                    consultadoFunc.setDetalhes(this.conectar.getResultSet().getString(3));
                    consultadoFunc.setQuantidades_horas(this.conectar.getResultSet().getString(4));
                    consultadoFunc.setTipo_servico(this.conectar.getResultSet().getString(5));
                }

                if(consultadoFunc.getFunc_id() == null) {
                    JOptionPane.showMessageDialog(null, "Verifique os dados digitados",
                    "Error!", JOptionPane.ERROR_MESSAGE);
                }
                
                else {
                    EditAlocatScreen telaEditAlo = new EditAlocatScreen();
                    telaEditAlo.configCampos(consultadoFunc);
                    telaEditAlo.alo_id = txtConsultaAlocacao.getText();
                    telaEditAlo.setVisible(true);
                    txtConsultaAlocacao.setText("");
                }
            } catch (Exception e) {
            } finally {
                this.conectar.fechaBanco();
            }
        }
    }//GEN-LAST:event_btnConsultarEmpresa1ActionPerformed

