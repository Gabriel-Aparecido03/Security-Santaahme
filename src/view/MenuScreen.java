    private void puxarListaEmpresa() {
        this.conectar.conectaBanco();
        
        try {
            this.conectar.executarSQL("SELECT empresa_razao_social,empresa_id from empresa");
            while(this.conectar.getResultSet().next()) {
                EmpresaAlocacao empresaDados = new EmpresaAlocacao();
                empresaDados.razaoSocial = this.conectar.getResultSet().getString(1);
                empresaDados.id = this.conectar.getResultSet().getString(2);
                listaEmpresa.add(empresaDados);
            }
        } catch (Exception e) {
            System.out.println(e);
        } finally {
            this.conectar.fechaBanco();
        }
    }
    
