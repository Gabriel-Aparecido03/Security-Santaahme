    private boolean validarCampos() {
        if(txtFuncCargo.getText().length() == 0 || 
                txtFuncCpf.getText().length() != 11 || 
                txtFuncEmail.getText().length() == 0 || 
                txtFuncNome.getText().length() == 0 ||
                Integer.parseInt(txtFuncSalario.getText()) <= 0) 
        {
            JOptionPane.showMessageDialog(null, "Error!",
            "Verifique os dados digitados", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
        
